     <h3 class="text-capitalize">Overview</h3><br>
<p align="justify">The Pattaya Floating Market is a one-of-a-kind experience that must not be missed during a visit to Pattaya. Established in 2008, the market is the biggest one in all of Thailand and prides itself as an authentic Thai experience for the visitors of Pattaya.

The market can be explored on a man-powered hired boat that one can rent for a small fee. The market is divided into four different sections, each one representing a part of the Thai kingdom. Within each section of the market, not only can one relish the local delicacies and shop the local handicrafts, but can also witness the difference in the architecture found in the various parts of the kingdom.

The market is also home to a wood-carving museum that offers a great opportunity to learn about the Thai culture..</p>
