<br>
		  <p class="text-center"><font size="4">The national food of Thailand is <b>Pad Thai</b>.We can get Pad Thai in street foods and as the main dish in resturants. Actually this food is a stir fried rice noodles dish prepared with chicken, shrimp or tofu.Thai cuisine is renowned worldwide for its harmonious blend of flavors and aromatic herbs and spices. It is characterized by dishes such as curries, stir-fries, soups, and salads that balance sweet, sour, spicy, and salty flavors. Rice is a staple food, and street food is an integral part of Thai culinary culture.</font></p>
