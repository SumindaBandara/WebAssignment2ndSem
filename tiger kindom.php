        <h3 class="text-capitalize">Overview</h3>
<p>Get up close and personal with some big cats at the Phuket Tiger Kingdom. That’s right, the TIger Kingdom allows visitors to enter the enclosures of the tigers and leopards they house, and interact with them. This includes being able to touch the big cat, and even get photographs clicked. The Tiger Kingdom breeds and raises most of the tigers under their supervision, ensuring their nutritional, psychological, and physical needs are met. The tigers that they house aren’t captured from the wild and since they have been raised in a controlled environment, they don’t possess the skills to survive in the wild. All this means that the Tiger Kingdom is not just an organisation offering animal-cruelty free fun, but it also means that by visiting here, you will be helping their cause to spread awareness about the condition of the big cats on our planet.</p>
