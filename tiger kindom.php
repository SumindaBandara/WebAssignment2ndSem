	   <h3 class="text-capitalize">Things to Know before you Visit:</h3>
			
		<img src="images/download.png" width="29" height="30"><p>The Tiger Kingdom charges different prices to enter the enclosure of different cats, ranging from leopards to different sized tigers. While these prices also include access to the rest of the park (not enclosures), it is not the other way around. Meaning, if you purchase a ticket to only enter the park, you will not have access to any of the enclosures.</p>
			
	    <img src="images/1 download.png" width="29" height="30"><p>They have a strict policy against feeding the animals in order to ensure proper nutrition for them. If you want to feed a tiger, it is best to check their website to see if there are any cubs at the Tiger Kingdom. Feeding the cubs is a highly demanded activity that is only available during certain months of the year.</p>
			
	     <img src="images/3 download.png" width="30" height="29"><p>The Phuket Tiger Kingdom regularly updates the prices of the activities they offer, without any prior notice. Thus, it is best to consult their website or your travel agent about the same.</p>
