<p>One of the major parts of the renowned Royal Grand Palace, the Temple of Emerald Buddha is among the famous religious spots of Southeast Asia. This temple is home to an enticing figurine of the Lord seated in a yogic posture, meditating. This might not be the largest of the statues of Buddha but it still never fails to inspire awe. This figurine of Lord Buddha was constructed using a single piece of green jade and is covered in diamonds and gold. The temple is nestled in the heart of a courtyard that showcases the remarkable craftsmanship and architecture from the Royal Era of Thailand. Also named as Wat Phra Kaew, the temple of Emerald Buddha is adorned with intricate sculptures, sophisticated paintings, and handicrafts. The unique structure and architecture are what makes this temple stand out among others.</p>
