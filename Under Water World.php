<p align="justify">The only aquarium in Pattaya, Underwater World offers visitors a chance to get acquainted with aquatic life, without having to venture into the water. From super-sized aquatic beasts found in the depths of the ocean to rare indegenous species, Underwater World presents an impressive collection of underwater life.<br>

The 100+ meter walkway positioned under the pool where the sea animals reside, offers visitors a surreal experience. From adults to young children, the aquarium is sure to mesmerise every visitor that comes through its doors.<br>
		The aquarium is divided into different sections, dedicated to different species of aquatic fauna. In fact, there are sections where visitors can physically touch the fish, or feed the Koi Fish located in another section.</p>
