 <h3><center>Planning a trip to Pattaya? We're here to help! Introducing Pattaya contains all the information you need: what to see, when to go, where to stay, and much more!</center></h3>
 <br>
 <br>

 <h4><p align="justify">Pattaya is a city on Thailand’s eastern Gulf coast known for its beaches. A quiet fishing village as recently as the 1960s, it’s now lined with resort hotels, high-rise condos, shopping malls, cabaret bars and 24-hour clubs. Nearby, hillside Wat Phra Yai Temple features an 18m-tall golden Buddha. The area also features several designer golf courses, some with views of Pattaya Bay.</p></h4>
 <br>
 <br>
 <h2><center>Why visit Pattaya?</center></h2>
 <br>
 <br>
 <p align="justify">The best things to do in Pattaya include attractions that range from ancient Thai traditions to modern adventures. This area on Thailand's eastern seaboard is where you can find the simple pleasures of the nation's most lively party street or be humbled by unique religious treasures.!</p>
 <br>
 <br>
  <h2><center>What to see & do in Pattaya?</center></h2>
 <h4>Featured stories & fun stuff</h4>
 <p align="justify">Things to do in Pattaya open the door to a thriving resort city full of beaches, water sports, nightlife, ancient shrines, tropical gardens, shopping experiences and museums. The Art in Paradise Pattaya museum is a unique art museum that's full of interactive 3D exhibits. At Underwater World Pattaya, you can get close to a wide variety of tropical sea life, and even go diving with sharks. You can also see quirky and unique exhibits like miniature replicas of world landmarks at Mini Siam. The Sanctuary of Truth is a world-famous and massive temple carved entirely out of wood.<br>

You can also relax on endless stretches of sand and surf at spots like Jomtien Beach and Pattaya Beach. If you want relaxing serenity, you can walk the themed landscapes at Nong Nooch Tropical Garden. The kids will love spots like Ramayana Water Park, The Million Years Stone Park & Pattaya Crocodile Farm and Sriracha Tiger Zoo.</p>
 
