	  <h1><center>Let's give attention to  Arts and Crafts of Thailand</center></h1>
	  
	  <br>
	  
	  <p><center><font size="5"><b>Thailand's arts and crafts have a rich and diverse cultural heritage that reflects the country's history, traditions, and religious beliefs. Thai craftsmanship is known for its intricate detailing, vibrant colors, and a harmonious blend of traditional and contemporary elements. Here is an overview of some prominent arts and crafts in Thailand.</b></font></center></p>
	  <br>
	  
	 <h4>Thai Skills</h4>
	    <p><center><font size="4">Thailand is renowned for its luxurious silk fabrics, woven from the cocoons of silkworms. The most famous silk-producing region is Isaan in northeastern Thailand. The vibrant colors and intricate patterns of Thai silk are highly sought after worldwide.</font></center></p>
	 <br>
	
		  <h4>Traditional Thai Painting</h4>
	    <p><center><font size="4">The general scenes of traditional Thai paintings are the depiction of historic events of the Buddha's life and Buddhist tales. However, the paintings still blend with the local Thai daily life, customs, culture, social tradition.Traditional Thai paintings usually consist of book illustrations, and painted ornamentation of buildings such as palaces and temples. Thai art, architecture and design is characterized by subtlety of design and form with considerable use of symbols, amulets, mystical drawings and both public and private statuary.</font></center></p>
		  
		  <br>
		  <h4>Wood Carving</h4>
	    <p><center><font size="4">Woodcarving may include figures from Hindu and Buddhist mythology such as Vishnu, Brahma, the singha or lion, the serpent naga. Also popular are scenes from Janaka tales and the life of the Buddha and from the Ramakien, the Thai version of the Indian Ramayana.</font></center></p>
	 <br>
		  <br>
		  <h4>Traditional Thai Costumes</h4>
	    <p><center><font size="4">Traditional Thai clothing is called chut thai, which literally means 'Thai outfit'. It can be worn by men, women, and children. Chut thai for women usually consists of a pha nung or a pha chung hang, a blouse, and a pha biang</font></center></p>
		  <br>
		   <h4>Buddha Statues</h4>
	    <p><center><font size="4">They are typically made of bronze, wood, or stone and are often adorned with intricate carvings and gold leaf. The traditional statues often feature a distinctive elongated earlobe, which represents the Buddha's detachment from worldly matters.</font></center></p>
		  <br>
		  <h4>Lacquerware</h4>
	    <p><center><font size="4">Lacquerware is a craft practiced by the Tai Khoen people who migrated down from the north. The basic structure is made from wood, mainly from bamboo, which is light in weight. The piece is then coated with multiple layers of a black tree sap, which is the lacquer, until the surface is smooth and shiny.</font></center></p>
