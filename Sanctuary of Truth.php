<p align="justify">Located at the mesmerising Rachvate cape of Pattaya, the views surrounding the Sanctuary Of Truth are only bested by the sheer magnificence of the temple/museum itself. Carved completely out of wood, the enormous structure is truly one-of-its-kind on the planet, and is a true testament of the architecture and craftsmanship of the Thai people.

The perfect place to add dollops of charm to your Instagram feed, the temple has designated photo spots all over the property. These spots have been scouted by the authorities and are regarded as the most photogenic spots at the property. The temple also offers a number of activities ranging from boat rides to elephant rides and like all Pattaya attractions worth their salt, hosts a couple of shows that are sure to spellbind every visitor.</p>
