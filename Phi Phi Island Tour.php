	   <h3 class="text-capitalize">Places to cover:</h3>
			
		<img src="images/download.png" width="29" height="30"><p><b>Maya Bay:</b>After reaching Koh Phi Phi early morning, commence your tour by taking the first stop at Maya Bay. This beach is surrounded by towering forest-clad limestone cliffs on three sides. This is the stop where you get to enjoy the best of a leisurely vacation with a hint of thrill. This beach earned its fame by virtue of the 1999 Hollywood blockbuster ‘The Beach’ that was shot here. Apart from soaking the alluring beauty of the beach, you can witness the colorful underwater world down below while snorkeling.</p>
			
	    <img src="images/1 download.png" width="29" height="30"><p><b>Monkey Beach:</b> After taking in the beauty that is resting under the cerulean waters of the Island, head to the Monkey Beach. This is another one of the great snorkeling spots for those who didn’t get enough views during their prior session. Fringed by emerald swaying trees, a long stretch of white sands, and the turquoise blue waters, this is the best spot to just relax and feel the warmth of the sun up high. Lesser crowds, zero commercialization, and thrilling water sports such as kayaking offer an absolute experience to tourists.</p>
			
	     <img src="images/3 download.png" width="30" height="29"><p><strong>Viking Cave</strong>: Phi Phi Island is not as tiny as it might sound. After relaxing for a while, head to the picturesque Viking Cave of the Island. Viking Cave is among the remarkable sites of the Phi Phi Island. Also known as Tham Phaya Nak in the local language, the southern walls of this cave adorn some intricate paintings that led to this name. These caves give one an opportunity to witness the harvesting of swiftlet birds’ nests which is actually the most profitable industry in Thailand. The nests are considered good for both skin and health.</p>
