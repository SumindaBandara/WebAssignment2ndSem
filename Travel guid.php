  <h3 class="text-center">Best time to visit </h3>
	<br>
	<br>
  <div class="row">
    <div class="col-lg-4">
      <p class="text-center"><strong>&nbsp;Cool Season (November to February):</strong></p>
<ul>
      <li>This is the most popular and ideal time to visit Thailand.</li>
        <li>The weather is relatively cooler and more comfortable, with less humidity.</li>
        <li>It is the peak tourist season, so expect larger crowds at popular tourist destinations.</li>
        <li>It's a great time for outdoor activities, sightseeing, and exploring cities and cultural sites.&nbsp;</li>
      </ul>
    </div>
    <div class="col-lg-4">
      <p class="text-center"><strong> Hot Season (March to May):</strong></p>
<ul>
      <li>During the hot season, temperatures can soar, especially in April.</li>
        <li>It is a good time to visit if you enjoy hot weather and want to explore less crowded attractions.</li>
        <li>Beach destinations are popular during this time for sunbathing and water activities.</li>
      </ul>
    </div>
    <div class="col-lg-4">
      <p class="text-center"><strong> &nbsp;Rainy Season (June to October):</strong></p>
<ul>
      <li>The rainy season brings frequent rainfall, but it doesn't usually rain all day every day.</li>
        <li>The countryside becomes lush and green, and waterfalls are at their best.</li>
        <li>This season offers the advantage of fewer tourists and lower hotel rates.&nbsp;</li>
        <li>Some beach destinations and islands may have limited activities and services during this time.&nbsp;</li>
      </ul>
