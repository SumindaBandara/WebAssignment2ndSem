	   <h3 class="text-capitalize">thai industries:</h3>
			
		<img src="images/1bullet.png" width="70" height="70"><p><strong>Manufacturing</strong>The manufacturing sector is a cornerstone of Thailand's economy. It encompasses various industries, such as automotive, electronics, machinery, and textiles. Thailand is one of the world's major automotive manufacturers, producing vehicles and automotive parts for both domestic and international markets. The electronics industry is another robust sector, manufacturing electronic components and devices for global supply chains.</p>
			
	    <img src="images/2bullet.png" width="70" height="70"><p><strong>Tourism</strong>: The tourism industry is a major economic driver for Thailand. The country's stunning landscapes, rich cultural heritage, and warm hospitality attract millions of international visitors each year. From bustling cities to serene beaches and historical sites, Thailand offers a diverse array of tourist attractions.</p>
			
	     <img src="images/3bullet.png" width="70" height="70"><p><strong>Agriculture</strong>: Agriculture has historically been a significant sector in Thailand. The country is one of the world's largest rice exporters and also produces other agricultural products like rubber, fruits, vegetables, sugarcane, and seafood.</p>
			
			 <img src="images/4bullet.png" width="70" height="70"><p><strong>Financial Services</strong>:  Thailand's financial services sector, including banking, insurance, and capital markets, plays a crucial role in the economy. The country has a well-developed financial system and is a regional financial hub, attracting foreign investments and supporting domestic businesses.</p>
			
		  <img src="images/5bullet.png" width="70" height="70"><p><strong>Food Processing</strong>: Thailand is renowned for its food processing industry, known for producing high-quality and flavorful food products. The sector includes processed seafood, canned fruits, canned vegetables, and various other ready-to-eat and ready-to-cook items.</p>
			
			 <img src="images/6bullet.png" width="70" height="70"><p><strong>Energy</strong>: Thailand's energy industry encompasses petroleum and natural gas production, as well as renewable energy sources like solar and wind power. The country is investing in expanding its renewable energy capacity to reduce reliance on fossil fuels.</p>
			
			 <img src="images/7bullet.png" width="70" height="70"><p><strong>Textiles and Garments</strong>: Thailand has a thriving textile and garment industry, manufacturing a wide range of products for both domestic consumption and exports.</p>
			
			 <img src="images/8bullet.png" width="70" height="70"><p><strong>Healthcare and Medical Tourism</strong>: Thailand is a popular destination for medical tourism, attracting international patients seeking quality healthcare services at competitive prices. The country's healthcare industry is growing, with advanced medical facilities and highly skilled professionals.</p>
			
			 <img src="images/9bullet.png" width="70" height="70"><p><strong>Information Technology</strong>: Thailand's information technology sector is growing rapidly, with a focus on software development, IT services, and digital innovation.</p>
			
			 <img src="images/10bullet.png" width="75" height="75"><p><strong>Real Estate and Construction</strong>:The real estate and construction industry is booming in Thailand, driven by residential, commercial, and infrastructure projects..</p>
