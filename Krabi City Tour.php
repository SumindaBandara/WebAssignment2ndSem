	   <h3 class="text-capitalize">Things to Know before you Visit:&nbsp;</h3>
			
		<img src="images/download.png" width="29" height="30"><p class="text-justify"><b>Tiger Cave Temple:</b>Start the Krabi city tour with one of the most iconic landmarks to give your day a much-needed kickstart. Head to Wat Tam Sue aka Tiger Cave Temple that is nestled amidst several hundred-year-old trees and mighty mountains. This temple is famed for not only being an extraordinary meditation center but also a center of interest for history aficionados. There are numerous monks worshipping and residing in the maze of naturally formed caves. This temple is based on the ancient teachings from the Buddhist texts and has walls covered in peculiar photos of cadavers and internal organs that help monks focus. The catch of this temple is that it is located atop 1,272 steps up a limestone tower which can be a little difficult for some.</p>
			
	    <img src="images/1 download.png" width="29" height="30"><p class="text-justify"><b>Khao Kanap Nam:</b> After some fitness activity of hiking and soaking in the beauty of the Andaman Sea from up top is the time to depart for Krabi Town and visit the ‘Khao Kanap Nam’. This is one of the major parts of the panoramic landscape of Krabi. This is a pair of limestone outcrops that are about a hundred meter high and stand proudly on either side of the river in Krabi. The stunning views from here leaves the jaws dropped.</p>
			
	     <img src="images/3 download.png" width="30" height="29"><p class="text-justify"><b>Ao Nam Mao Outdoor Aquarium:</b> If you are one of the wildlife lovers, the next stop will leave you in awe. After witnessing the scenic vistas of Krabi. It is time to behold the beauty of the underwater world at the Krabi Aquarium. Also named as the Ao Nam Mao Outdoor Aquarium, this place is heaven on earth for the little ones. This aquarium boasts about the breeding of the endangered species in the nearby saltwater lakes. Ao Nam Mao blankets an observation deck that showcases the views of several large pools housing various marine species.</p>
