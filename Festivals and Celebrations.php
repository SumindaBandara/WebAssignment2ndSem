	  <h1><center>Festivals and Celebrations in Thailand
	  </center></h1>
	  
	  <br>
	  
	  <p><center><font size="4">Thailand's calendar is punctuated with vibrant and colorful festivals that celebrate religious, cultural, and historical events. <b>Songkran</b>, the Thai New Year, is celebrated in April with exuberant water fights and symbolic cleansing rituals. Loy Krathong, the Festival of Lights, is held in November when people release small, beautifully decorated baskets made of banana leaves and flowers into rivers and waterways to pay respects to the goddess of water. Yi Peng, the Lantern Festival, takes place at the same time as Loy Krathong, where the sky is adorned with thousands of glowing lanterns that symbolize letting go of negativity and starting a new.</font>
	  </center></p>
	  <br>
		  <p> <font size="4">Here are some of the most significant festivals celebrated throughout Thailand</font></p>
		  <br>
		  <h4>Songkran Festival(Water Festival)</h4>
	    <p><center><font size="4">Also known as Songkran Festival, the Water Festival Thailand is perhaps one of the most fun festivals in the country, and possibly in the world! This long and large festival involves a 3-day water fight in which people participate across the country. With drinking, music, dancing, and people drenched from head to toe, people use buckets, hose pipes, water guns, and anything else that they can get their hands on to use to splash water on others. Sounds fun?
		<br>
		Held On: April 13th to 15th 2022</font></center></p>

	 	 <br>
		  <h4>Phi Ta Khon (Ghost Festival)</h4>
	    <p><center><font size="4">Also known as the Ghost Festival, this is one of the most interesting Thailand traditional festivals. Phi Ta Khon is a festival that combines religious traditions, a fun-loving party atmosphere, and local handicrafts. It is celebrated for three days with thousands of locals wearing colorful, ghastly masks with stretched faces and phallic noses, painted in bright, and gaudy colors. The Ghost Festival’s origin is a mixture of Buddhist and animist beliefs. Friday is usually when the main parade takes place, Saturday has music shows and pageants while Sunday is reserved for Buddhist ceremonies. Consider attending this one if you want to experience the vibe of religious festivals in Thailand.
	<br>
	Location: Dan Sai Town
	<br>
	Held On: Annually in June</font></center></p>
		   <br>
		  <h4>Lopburi Monkey Banquet (Food Festival)</h4>
	    <p><center><font size="4">This is a Thailand food festival with a difference. Located at a distance of 150 kms in the north of Bangkok, this festival takes place in Lopburi. It is famous for the many monkeys that reside there, and one of the most popular activities for tourists when they visit Lopburi is feeding the simians. However, come November and the simple treats that they get from visitors are replaced by something a lot more grand. A huge banquet is laid out for about 3,000 monkeys that live in this part of Thailand, giving them a day to feast on 4 tons of fruits, vegetables and an array of other treats. This is one of the most interesting Thailand festivals.
	`	<br>
	Held In: November</font></center></p>
		   <br>
		  <h4>Yi Peng (The Lantern Festival)</h4>
	    <p><center><font size="4">If you want to see even more spectacular Thailand festivals and celebrations, you should plan your trip around the festival of Yi Peng or the Lantern Festival. Like in countries like China and Vietnam, Chiang Mai’s Lantern Festival is one of the most amazing sights you will ever see. People gather down by the banks of the Peng River and release thousands of paper lanterns into the sky and watch them float away in the gentle evening breeze. Although it is a more of a tradition than a festival, it is definitely a fantastic photo op for capturing some beautiful memories. You can also witness religious ceremonies, firework displays, parades, and the releasing of paper boats in the river. Be a part of the Lantern Festival Thailand 2022 if you want to know the culture of this country better. Amongst all the Thai festivals, this is certainly one of the most vibrant ones.
		<br>
		Held On: 8th to 9th November, 2022</font></center></p>
		  <br>
		  <h4>Boon Bang Fai (Rocket Festival)</h4>
	    <p><center><font size="4">Also known as the Rocket Festival, this is a famous festival in Thailand that is significant to the farming communities of Issan, which is located in the north-eastern part of the country. Entire villages attend the festival because they are considered the last big ‘knees up’ before the start of the planting season. The rockets are shot up to encourage the Gods to send down plenty of rainfall. Elaborate rockets are built and paraded around on the first day of Boon Bang Fai, and then launched high into the air throughout the weekend. One of the best festivals in Thailand 2022, every Rocket Festival is accompanied by live folk music, beauty pageants, drinking of local rice wine, and a party full of fun and frolic.
		<br>
		Held In: Annually in mid May</font></center></p>
