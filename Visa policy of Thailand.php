	<h3>Visa types:</h3>
<h5>General visa types</h5><br><ul>
<li>Courte Upon official request, the Royal Thai Embassy may grant courtesy visas/laissez-passer to diplomats/UN officials/others who wish to enter the Kingdom on official duty and/or other purposes</li>
<li>Permanent resident: To qualify for this visa, you must have stayed in Thailand for 3 consecutive years with the one-year visa extensions. If married to a Thai for 5 years, one must be earning 30,000 baht/month. If single, your monthly income must be 80,000 baht minimum.</li>
<li>Tourist: If an individual wishes to remain in Thailand for more than 30 days, he/she may wish to obtain a tourist visa at a Royal Thai Embassy or Consulate prior to arriving in Thailand. The tourist visa must be used within validity date, that varies with the number of entries purchased, and allows an initial stay of 60 days. After arrival in Thailand, a tourist visa or a visa exempt entry may be extended once for an additional 30 days according to immigration Bureau order 327/2557. There is a 1,900 baht fee for each extension.</li>
<li>Transit: This type of visa is issued to applicants who wish to enter the Kingdom for the following purposes: to travel in transit through the Kingdom in order to proceed to the country of destination or to re-enter his/her own country (category "TS"); the person in charge of or crew of a conveyance coming to a port, station, or area in the Kingdom (category "C")</li></ul>
<h5>Non-Immigrant visa types</h5><ul>
<li>Type B: to conduct business; work; attend business conferences; teach school; attend scuba diving, muay Thai, and massage courses</li>
<li>Type D: diplomatic visa</li>
<li>Type ED: to study; to come on a work study tour or observation tour; to participate in projects or seminars; to attend a conference or training course; to study as a foreign Buddhist monk</li>
<li>Type EX: to undertake skilled work or to work as an expert or specialist</li>
<li>Type F: to perform official duties, e.g., military exercises or governmental assignments</li>
<li>Type IB: to invest or perform other activities relating to investment, subject to the provision of the established laws on investment promotion</li>
<li>Type IM: to invest with the concurrence of the Thai ministries and governmental departments concerned</li>
<li>Type M: to work as a film-producer, journalist, or reporter with permission from the Ministry of Foreign Affairs</li>
<li>Type O: to visit family; to perform duties for a state enterprise or social welfare organizations (NGOs); to receive medical treatment; to be a sports coach as required by the Thai government; to be a contestant; to be a witness in a judicial process</li>
<li>Type O-A: retirement visa</li>
<li>Type R: to perform missionary work or other religious activities with the concurrence of the Thai ministries or government departments concerned</li>
<li>Type RS: to conduct scientific research or training or teaching in a research institute</li>
<li>Type S:to participate in an officially recognised sports event.</li>  
	  </ul> 
	 <h4> Work permit</h4>
<p class="text-justify">To legally work in Thailand, a foreigner must apply for a work permit. Work permit is a legal document that states a foreigner's position, current occupation, or job description and the Thai company he is working with. It also serves as a license to perform a job or an occupation allowed for foreigners inside Thailand. A foreigner is eligible to apply for a work permit as long as he has a non-immigrant visa or a resident visa, has an available employer who will provide documents for work permit, and the occupation he will perform is not prohibited to foreigners.</p></div><br>
