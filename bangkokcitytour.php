	   <h3 class="text-capitalize">Places to cover:</h3>
			
		<img src="images/download.png" width="29" height="30"><p class="text-justify"><b>Golden Buddha:</b>Start your day in Bangkok with a visit to the statue of Golden Buddha. Sitting in Wat Traimit is a 5.5 tonne seated statue of Lord Buddha. Dating back to the 13th century, the statue was originally enveloped in plaster. The fact that it is actually made out of gold was revealed only after a worker dropped the statue accidentally. If the magnificence of the statue is not enough, there is a museum, a video presentation, and 3D presentations. With such a fascinating history and plenty to do, a visit to Wat Traimit will give a kickstart to your day.</p>
			
	    <img src="images/1 download.png" width="29" height="30"><p class="text-justify"><b>Marble Temple:</b> Next head to the legendary Marble Temple. Also known as Wat Benchamabophit, Marble temple is yet another Buddha temple covering the floors of Bangkok. This iconic structure has its imprint on the Thai 5B coin. This temple adorns Italy imported white marble with a traditional Thai-style roof of red and gold. The temple was built around the 19th century under the command of Rama V and appeals to the history aficionados as well as the design students alike. This will definitely be the most intriguing spot in Bangkok.</p>
			
	     <img src="images/3 download.png" width="30" height="29"><p class="text-justify"><b>Gems Gallery:</b>Lastly, end your sightseeing at the renowned Gems Gallery of Bangkok. If there is a twinkle in your eye when you see something shimmery, this gallery would be heaven on earth for you. Home to several precious stones and gems, this gallery is the worldâ€™s biggest jewelry center. With stupendous raw materials, the outstanding craftsmanship of Thailand, fair prices, and the ability to serve every need of a gem lover, this gallery is truly one of its kind. Ending the Bangkok city tour at this spot would surely just complete your day in this bustling city.</p>
