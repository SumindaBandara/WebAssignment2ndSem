	   <h3 class="text-capitalize">Places to cover:&nbsp;</h3>
			
		<img src="images/download.png" width="29" height="30"><p class="text-justify"><b>Railay Beach:</b>Start the tour early by heading to Railay Beach. This beach is a paradise for rock climbers out there. The lush greens covering the floors of this white sand beach takes away the breath of spectators. Stroll around the beach, try out some rock climbing if you have your gear, or just sit and relax your vacation away from the bustling city culture.</p>
			
	    <img src="images/1 download.png" width="29" height="30"><p class="text-justify"><b>Tub Island:</b> Krabi is full of fun names. We have already covered Chicken, how about the tub now. The next stop is the Tub Island that is also named as Koh Tap in the local language. When the tides are pretty low, one can easily travel from Chicken Island to Tub Island using the sandbar aka Talay Waek. The sands of this island are covered in corals and beautiful shells. The panoramic views of the surrounding nature marvels are a sight to behold.</p>
			
	     <img src="images/3 download.png" width="30" height="29"><p class="text-justify"><b>Chicken Island:</b> Next head to perhaps the most photogenic islands of the tour, the Chicken Island. Also named as Koh Kai, the island got its name by virtue of the chicken-shaped rock that forms the southern tip of the island. Just imagine witnessing a limestone formation in the shape of a chicken. Another peculiarity about this island is the sandbar that connects it to Koh Mor and Koh Tap. Visible during the low tides, this sandbar gives one an opportunity to cross one island from another with the crystal water surrounding them.</p>
