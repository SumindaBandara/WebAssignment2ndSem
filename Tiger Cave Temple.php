	      <h3 class="text-capitalize">Overview</h3>
<p class="text-justify">Surrounded by mysterious history and magnificent views alike, the Tiger Cave Temple is one of the most popular destinations in Krabi. Accessible only to those willing to conquer a grueling flight of 1237 stars, the grandeur of the temple, combined with 360-degree panoramic views of lush green beauty surrounding the limestone structure is well worth the effort.

The temple is blessed with views of the Andaman Sea, vast expanses of tropical rainforests, and otherworldly natural limestone formations. Truly a prime example of the fusion of natural beauty and man-made marvels, Tiger Cave Temple, locally known as Wat Tham Sua, is a spiritual destination that is bound to make you feel at peace, despite the challenge involved in reaching the temple</p>
